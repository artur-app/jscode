function makeSum()
{
    let NuOne = document.getElementById("numberOne").value;
    let NuTwo = document.getElementById("numberTwo").value;
    document.getElementById("Result").textContent = NuOne + NuTwo;
    document.write (NuOne + NuTwo);
}


function makeMult()
{
    let NuOne = document.getElementById("numberOne").value;
    let NuTwo = document.getElementById ("numberTwo").value;
    document.getElementById("Result").textContent = NuOne * NuTwo;
    document.write (NuOne + NuTwo);
}